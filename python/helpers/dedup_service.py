def check_should_respond(
    issue_number: int,
    triggering_comment_id: int,
    triggering_comment_body: str,
    triggering_comment_user: str,
    triggering_comment_user_type: str,
    all_comments: list
) -> Dict[str, Any]:
    """
    Comprehensive check for whether to respond to a comment.
    
    This is the main function that repository_automation._answer_comment should call.
    It performs all checks and returns a detailed result.
    
    Args:
        issue_number: Issue number
        triggering_comment_id: ID of the comment that triggered this check
        triggering_comment_body: Body of the triggering comment
        triggering_comment_user: Username of the triggering comment author
        triggering_comment_user_type: User type of the triggering comment author
        all_comments: List of all comments on the issue
        
    Returns:
        Dict with keys:
            - respond: bool - True if should respond
            - reason: str - Explanation
            - hash_id: str - Hash to use if responding
            - trigger_id: str - Trigger ID used for hash generation
    """
    result = {
        "respond": False,
        "reason": "",
        "hash_id": "",
        "trigger_id": ""
    }
    
    # Step 1: Check if we should respond at all
    should_respond, reason = should_respond_to_comment(
        triggering_comment_body,
        triggering_comment_user,
        triggering_comment_user_type,
        all_comments
    )
    
    if not should_respond:
        result["reason"] = reason
        return result
    
    # Step 2: Generate trigger ID (what are we responding to?)
    trigger_id = f"reply_to_{triggering_comment_id}"
    result["trigger_id"] = trigger_id
    
    # Step 3: Generate hash for this response
    hash_id = generate_dedup_hash(issue_number, trigger_id)
    result["hash_id"] = hash_id
    
    # Step 4: Check if we've already responded with this hash
    if has_handled_comment(all_comments, target_hash=hash_id):
        result["reason"] = f"already_responded:{hash_id}"
        return result
    
    # All checks passed - should respond
    result["respond"] = True
    result["reason"] = "ok"
    return result


