# Context Manager for Tracing
# =============================================================================

class TracingContext:
    """Context manager for automatic span management."""
    
    def __init__(
        self,
        observer: ObserverMesh,
        operation_name: str,
        trace_id: Optional[str] = None,
        parent_span_id: Optional[str] = None,
        tags: Optional[Dict[str, str]] = None
    ):
        self.observer = observer
        self.operation_name = operation_name
        self.trace_id = trace_id
        self.parent_span_id = parent_span_id
        self.tags = tags
        self.span: Optional[Span] = None
    
    async def __aenter__(self) -> Span:
        self.span = await self.observer.tracer.start_span(
            operation_name=self.operation_name,
            trace_id=self.trace_id,
            parent_span_id=self.parent_span_id,
            tags=self.tags,
        )
        return self.span
    
    async def __aexit__(self, exc_type, exc_val, exc_tb) -> None:
        if self.span:
            status = "error" if exc_type else "ok"
            await self.observer.tracer.end_span(self.span.span_id, status)
            
            if exc_type:
                await self.observer.tracer.add_span_log(
                    self.span.span_id,
                    f"Exception: {exc_val}",
                    {"exception_type": exc_type.__name__ if exc_type else None}
                )
