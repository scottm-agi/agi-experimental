"""
Deduplication Service for Repository Automation

A centralized module for detecting duplicate events, self-comments, and
preventing infinite loops in webhook-triggered workflows.

This module provides deterministic functions that both webhook_handler.py
and repository_automation.py can use to check if an event should be processed.

Key Concepts:
- comment_id: The unique ID of a comment from GitHub/Forgejo API
- hash_id: A generated hash for deduplication (format: <!-- agix-id: {hash} -->)
- trigger_id: Identifies what triggered the response (e.g., "initial" or "reply_to_{id}")

Usage:
    from python.helpers.dedup_service import (
        is_self_generated_comment,
        should_respond_to_comment,
        extract_agix_hash,
        generate_dedup_hash,
        has_handled_comment
    )
"""

import re
import logging
from typing import Dict, Any, Optional, Tuple

logger = logging.getLogger("dedup-service")

# =============================================================================
# Constants
# =============================================================================

# Markers that indicate a comment was generated by the system
SELF_MARKERS = [
    "<!-- agix-id:",
    "agix-id:",
    "🤖 Comment Assistant:",
    "🎯 Expert Solution Analysis:",
    "AI-HANDLED:",
    "<!-- AI-ANALYSIS",
]

# Bot account patterns (case-insensitive)
BOT_PATTERNS = [
    "[bot]",
    "-bot",
    "_bot",
    "bot-",
    "automation",
]


# =============================================================================
# Core Detection Functions
# =============================================================================


def is_self_generated_comment(comment_body: str) -> bool:
    """
    Check if a comment body contains markers indicating it was generated by the system.
    
    This is the PRIMARY check for preventing infinite loops. If a comment has any
    of these markers, the system should NOT generate a new response.
    
    Args:
        comment_body: The raw text content of the comment
        
    Returns:
        True if the comment was generated by the system, False otherwise
        
    Examples:
        >>> is_self_generated_comment("Hello world")
        False
        >>> is_self_generated_comment("Analysis complete <!-- agix-id: abc123 -->")
        True
        >>> is_self_generated_comment("🤖 Comment Assistant: Here's my analysis...")
        True
    """
    if not comment_body:
        return False
    
    for marker in SELF_MARKERS:
        if marker in comment_body:
            logger.debug(f"[DEDUP] Self-generated comment detected (marker: {marker})")
            return True
    
    return False


def is_bot_account(username: str, user_type: str = "") -> bool:
    """
    Check if a user account appears to be a bot.
    
    Args:
        username: The login/username of the commenter
        user_type: The user type field from API (e.g., "User", "Bot")
        
    Returns:
        True if the account appears to be a bot
        
    Examples:
        >>> is_bot_account("dependabot[bot]", "Bot")
        True
        >>> is_bot_account("your-org", "User")
        False
        >>> is_bot_account("github-actions[bot]", "Bot")
        True
    """
    if not username:
        return False
    
    # Explicit bot type
    if user_type and user_type.lower() == "bot":
        return True
    
    username_lower = username.lower()
    for pattern in BOT_PATTERNS:
        if pattern in username_lower:
            return True
    
    return False


def extract_agix_hash(comment_body: str) -> Optional[str]:
    """
    Extract the agix-id hash from a comment body.
    
    Args:
        comment_body: The raw text content of the comment
        
    Returns:
        The hash string if found, None otherwise
        
    Examples:
        >>> extract_agix_hash("Hello <!-- agix-id: abc123def -->")
        'abc123def'
        >>> extract_agix_hash("No hash here")
        None
    """
    if not comment_body:
        return None
    
    # Match both formats: <!-- agix-id: xxx --> and agix-id: xxx
    patterns = [
        r'<!-- agix-id:\s*(\w+)\s*-->',
        r'agix-id:\s*(\w+)',
    ]
    
    for pattern in patterns:
        match = re.search(pattern, comment_body)
        if match:
            return match.group(1)
    
    return None


def generate_dedup_hash(issue_number: int, trigger_id: str = "initial") -> str:
    """
    Generate a deduplication hash for a response.
    
    The hash is deterministic based on issue number and trigger, ensuring
    that the same response attempt produces the same hash.
    
    Args:
        issue_number: The issue number being responded to
        trigger_id: What triggered this response (e.g., "initial", "reply_to_123")
        
    Returns:
        A 12-character hash string
        
    Examples:
        >>> generate_dedup_hash(42, "initial")
        '9f86d081884c'  # Example - actual hash is deterministic
        >>> generate_dedup_hash(42, "reply_to_100")
        'a3c35f4e5d8c'  # Different hash for different trigger
    """
    from python.helpers.hashing import content_hash_short
    content = f"{issue_number}:{trigger_id}"
    return content_hash_short(content, length=12)


def generate_hash_tag(hash_id: str) -> str:
    """
    Generate the HTML comment tag for embedding in responses.
    
    Args:
        hash_id: The deduplication hash
        
    Returns:
        HTML comment string
        
    Examples:
        >>> generate_hash_tag("abc123")
        '\\n\\n<!-- agix-id: abc123 -->'
    """
    return f"\n\n<!-- agix-id: {hash_id} -->"


