def should_respond_to_comment(
    comment_body: str,
    comment_user: str = "",
    comment_user_type: str = "",
    issue_comments: list = None
) -> Tuple[bool, str]:
    """
    Determine if the system should generate a response to this comment.
    
    This is the main entry point for checking if we should respond.
    It runs all relevant checks and returns a decision with reason.
    
    Args:
        comment_body: The body of the triggering comment
        comment_user: Username of the commenter
        comment_user_type: User type (e.g., "User", "Bot")
        issue_comments: Optional list of all comments on the issue (for context)
        
    Returns:
        Tuple of (should_respond: bool, reason: str)
        
    Examples:
        >>> should_respond_to_comment("Please help", "your-org", "User")
        (True, "ok")
        >>> should_respond_to_comment("Analysis complete <!-- agix-id: xxx -->", "agix-bot")
        (False, "self_generated")
    """
    # Check 1: Self-generated comment
    if is_self_generated_comment(comment_body):
        return (False, "self_generated")
    
    # Check 2: Bot account
    if is_bot_account(comment_user, comment_user_type):
        return (False, f"bot_account:{comment_user}")
    
    # Check 3: If we have the full comment list, check if last N comments are all self-generated
    # This catches edge cases where multiple bot responses exist
    if issue_comments and len(issue_comments) > 0:
        consecutive_self = 0
        for c in reversed(issue_comments):
            body = c.get("body", "") if isinstance(c, dict) else ""
            if is_self_generated_comment(body):
                consecutive_self += 1
            else:
                break
        
        # If last 3+ comments are all self-generated, something is wrong - stop
        if consecutive_self >= 3:
            return (False, f"loop_detected:{consecutive_self}_consecutive_self")
    
    return (True, "ok")


def find_last_human_comment_id(comments: list) -> Optional[int]:
    """
    Find the ID of the last comment that was NOT made by the system.
    
    This is used to generate a proper trigger_id for deduplication hashes.
    
    Args:
        comments: List of comment dicts with 'id', 'body', 'user' fields
        
    Returns:
        The comment ID of the last human comment, or None if all are self-generated
    """
    if not comments:
        return None
    
    for c in reversed(comments):
        body = c.get("body", "")
        user = c.get("user", {})
        username = user.get("login", "") if isinstance(user, dict) else ""
        user_type = user.get("type", "") if isinstance(user, dict) else ""
        
        if not is_self_generated_comment(body) and not is_bot_account(username, user_type):
            return c.get("id")
    
    return None


def has_handled_comment(
    comments: list,
    target_hash: str = None,
    expected_trigger_id: str = None,
    issue_number: int = None
) -> bool:
    """
    Check if we've already responded to comments with the given hash or trigger.
    
    This queries the existing comments to see if a response with our hash exists.
    
    Args:
        comments: List of comment dicts with 'body' field
        target_hash: Specific hash to look for
        expected_trigger_id: If no target_hash, compute from trigger_id + issue_number
        issue_number: Issue number for hash computation
        
    Returns:
        True if a response with matching hash already exists
    """
    if not comments:
        return False
    
    # Compute hash if not provided directly
    if not target_hash and expected_trigger_id and issue_number:
        target_hash = generate_dedup_hash(issue_number, expected_trigger_id)
    
    if not target_hash:
        logger.warning("[DEDUP] has_handled_comment called without hash or trigger_id")
        return False
    
    # Check all comments for this hash
    for c in comments:
        body = c.get("body", "")
        found_hash = extract_agix_hash(body)
        if found_hash == target_hash:
            logger.info(f"[DEDUP] Found existing response with hash {target_hash}")
            return True
    
    return False


